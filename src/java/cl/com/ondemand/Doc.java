package cl.com.ondemand;

public class Doc {
	
	private String ApplGrpNombre;
	private String ApplNombre;
	private String DisplayValor;
	private String DocId;
	private String DocUbicacion;
	private String DocTipo;
	private String DocNombre;
	private String FolderNombre;
	private byte [] Documento;
	
	public String getApplGrpNombre() {
		return ApplGrpNombre;
	}
	public void setApplGrpNombre(String applGrpNombre) {
		ApplGrpNombre = applGrpNombre;
	}
	public String getApplNombre() {
		return ApplNombre;
	}
	public void setApplNombre(String applNombre) {
		ApplNombre = applNombre;
	}
	public String getDisplayValor() {
		return DisplayValor;
	}
	public void setDisplayValor(String displayValor) {
		DisplayValor = displayValor;
	}
	public String getDocId() {
		return DocId;
	}
	public void setDocId(String docId) {
		DocId = docId;
	}
	public String getDocUbicacion() {
		return DocUbicacion;
	}
	public void setDocUbicacion(String docUbicacion) {
		DocUbicacion = docUbicacion;
	}
	public String getDocTipo() {
		return DocTipo;
	}
	public void setDocTipo(String docTipo) {
		DocTipo = docTipo;
	}
	public String getDocNombre() {
		return DocNombre;
	}
	public void setDocNombre(String docNombre) {
		DocNombre = docNombre;
	}
	public String getFolderNombre() {
		return FolderNombre;
	}
	public void setFolderNombre(String folderNombre) {
		FolderNombre = folderNombre;
	}
	public byte[] getDocumento() {
		return Documento;
	}
	public void setDocumento(byte[] documento) {
		Documento = documento;
	}
	
	
}
